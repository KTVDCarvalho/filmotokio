package filmotokio.domain;

/**
 * Enum for film production roles
 * Each person has one role type
 */
public enum TypePersonEnum {
    // Writes screenplay/script for the film
    SCREENWRITER,
    
    // Creates or performs music for the film
    MUSICIAN,
    
    // Handles camera work and cinematography
    PHOTOGRAPHER,
    
    // Acts/performs in the film
    ACTOR,
    
    // Directs the film
    DIRECTOR
}
